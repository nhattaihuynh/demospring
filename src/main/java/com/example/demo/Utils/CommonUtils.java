    public static char removeAccentForAChar(char ch) {
        int index = Arrays.binarySearch(FROM_CHARACTERS, ch);
        if (index >= 0) {
            ch = TO_CHARACTERS[index];
        } else {
            for (int i = 0; i < FROM_CHARACTERS.length; i++) {
                if (FROM_CHARACTERS[i] == ch) {
                    index = i;
                    if (ch >= 0) {
                        ch = TO_CHARACTERS[index];
                    }
                }
            }
        }
        return ch;
    }


    public static String removeAccent(String s) {
        StringBuilder sb = new StringBuilder(s);
        for (int i = 0; i < sb.length(); i++) {
            sb.setCharAt(i, removeAccentForAChar(sb.charAt(i)));
        }
        return sb.toString();
    }
